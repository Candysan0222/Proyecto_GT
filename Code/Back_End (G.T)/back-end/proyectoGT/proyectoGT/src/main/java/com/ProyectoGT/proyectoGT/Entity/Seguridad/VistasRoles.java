package com.ProyectoGT.proyectoGT.Entity.Seguridad;

import com.ProyectoGT.proyectoGT.Entity.CamposAuditoria;

import jakarta.persistence.Entity;
import jakarta.persistence.JoinColumn;
import jakarta.persistence.ManyToOne;
import jakarta.persistence.Table;

@Entity
@Table(name="vistas_roles")
public class VistasRoles extends CamposAuditoria{

	@ManyToOne
	@JoinColumn(name="rol_id", nullable = false, unique=true)
	private Roles rolId;
	
	@ManyToOne
	@JoinColumn(name="vista_id", nullable = false, unique=true)
	private Vistas vistaId;

	public Roles getRolId() {
		return rolId;
	}

	public void setRolId(Roles rolId) {
		this.rolId = rolId;
	}

	public Vistas getVistaId() {
		return vistaId;
	}

	public void setVistaId(Vistas vistaId) {
		this.vistaId = vistaId;
	}
	
	
	
}
